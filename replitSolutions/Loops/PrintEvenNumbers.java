package Loops;

public class PrintEvenNumbers {
    public static void main(String[] args) {
    int start= 80;
    int end =0;
    for(int i=start; i>=end;i=i-2){
        System.out.print(i);
        if(i!=end){
            System.out.print(" ");
        }
    }




    }
}
